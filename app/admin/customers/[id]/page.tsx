const RECEIPT_ELIGIBLE_INVOICE_START_DATE = '2026-04-01';
const AUTO_GENERATED_RECEIPT_NOTE_PATTERN = /(وذلك عن فاتورة\s+(?:المحل|المخزن)\s+رقم[\s\S]*$|وذلك عن قسط[\s\S]*$)/;

function stripAutoGeneratedInvoiceNotes(notes: string): string {
  return notes.replace(AUTO_GENERATED_RECEIPT_NOTE_PATTERN, '').trim();
}

function isInvoiceEligibleForReceipt(invoiceDate?: string): boolean {
  if (!invoiceDate) return false;
  const parsed = new Date(invoiceDate);
  if (isNaN(parsed.getTime())) return false;
  return parsed >= new Date(RECEIPT_ELIGIBLE_INVOICE_START_DATE);
}

